# 100回ループを実行する
for i in range(100):
    # 現在の回数 (i + 1) が10の倍数かどうかを判定する
    # 10で割った余りが0であれば、10の倍数である
    if (i + 1) % 10 == 0:
        # 10の倍数の場合（10, 20, 30, ...）、お祝いメッセージを表示する
        print(f"🎉 おめでとう！ {i + 1}回目のハローワールドです！ 🎉")
    else:
        # それ以外の回では、通常のメッセージを表示する
        print(f"Hello, World! {i + 1}")
